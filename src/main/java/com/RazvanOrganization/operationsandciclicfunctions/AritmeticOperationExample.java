package com.RazvanOrganization.operationsandciclicfunctions;

public class AritmeticOperationExample {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = 7;

        // Suma numerelor
        int suma = a + b;
        System.out.println("Suma este: " + suma);
        System.out.println("Suma este: " + (a + b));

        // Diferenta numerelor
        int dif = a - b;
        System.out.println("Diferenta este: " + dif);

        // Inmultirea numerelor
        int inm = a * b;
        System.out.println("Inmultirea este: " + inm);

        // Impartirea numerelor
        int imp = a / b;
        System.out.println("Impartirea este: " + imp);

        // Restul impartirii numerelor
        int rest = a % c;
        System.out.println("Restul impartirii este: " + rest);

    }
}
